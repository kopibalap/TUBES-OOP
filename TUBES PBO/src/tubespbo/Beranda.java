public void showTiket(String nama){
      Connection koneksi = conn.connectDB();
        try {
            PreparedStatement ps = (PreparedStatement)koneksi.prepareStatement("SELECT * FROM tiket WHERE nama_penumpang = ?");
            ps.setString(1, nama);
          
            ResultSet rs = ps.executeQuery();
   
            ResultSetMetaData stData = rs.getMetaData();
            q = stData.getColumnCount();
            DefaultTableModel RecordTable = (DefaultTableModel)jTable1.getModel();
            RecordTable.setRowCount(0);
            
            while(rs.next()){
                Vector columdData = new Vector();
                for(int i = 1;i <= q; i++ ){
                    columdData.add(rs.getString("id"));
                    columdData.add(rs.getString("nama_penumpangs"));
                    columdData.add(rs.getString("umur"));
                    columdData.add(rs.getString("jenis_kelamin"));
                    columdData.add(rs.getString("nama_kereta"));
                    columdData.add(rs.getString("gerbong"));
                    columdData.add(rs.getString("kelas"));
                    columdData.add(rs.getString("jadwal_berangkat"));
                    columdData.add(rs.getString("jadwal_tiba"));
                    columdData.add(rs.getString("stasiun_asal"));
                    columdData.add(rs.getString("stasiun_tujuan"));
                
                }
                RecordTable.addRow(columdData);
            }
          
          
        } catch (SQLException ex) {
            Logger.getLogger(Penumpang.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
