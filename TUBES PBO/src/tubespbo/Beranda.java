                            gerbong = rs.getString("gerbong");
                            
                            kelasLabel.setText("Kelas : " + rs.getString("kelas"));
                            kelas = rs.getString("kelas");
                            
                            berangkatLabel.setText("Jadwal Berangkat : " + rs.getString("jadwalBerangkat"));
                            jadwalBerangkat = rs.getString("jadwalBerangkat");
                            
                            tibaLabel.setText("Jadwal Tiba : " + rs.getString("jadwalTiba"));
